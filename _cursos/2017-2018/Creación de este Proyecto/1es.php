<h1>Creaci&oacute;n de este Proyecto</h1>

<p>Ya que el centro educativo IES Delgado Hern&aacute;ndez, a pesar de disponer de su propia p&aacute;gina web, &eacute;sta no dispone de secci&oacute;n donde documentar y publicar todas las actividades y proyectos que se realizan cada a&ntilde;o.</p>

<p>De esta forma, a dicha informaci&oacute;n, le&nbsp;falta&nbsp;el dinamismo, interactividad e interoperabilidad que ofrece una aplicaci&oacute;n web.</p>

<p>Por ello surge la idea de realizar dicho proyecto, oportunidad brindada por el profesor del m&oacute;dulo Implantaci&oacute;n de Aplicaciones Web (Sr. Domingo P&eacute;rez Duque).</p>

<p>El objetivo de esta aplicaci&oacute;n es la implementaci&oacute;n de un sistema de gesti&oacute;n y control de las actividades extraescolares del centro&nbsp;mediante una aplicaci&oacute;n web</p>

<p>Se pretende ayudar y facilitar a los usuarios de la plataforma la gesti&oacute;n y consulta de dichas actividades. Adem&aacute;s, a trav&eacute;s de ella se dejar&aacute; constancia de las opiniones de los interesados mediante el sistema de comentarios introducido en la aplicaci&oacute;n.</p>

<p>Otro punto importante en el objetivo se basa en que en su desarrollo incrementemos considerablemente los conocimientos y podamos al mismo tiempo adquirir h&aacute;bito y soltura en el desarrollo web.</p>

<p>El desarrollo de este proyecto ha requerido de mucho tiempo, esfuerzo y dedicaci&oacute;n.&nbsp;</p>

